$('#february').keyup(function() {
	if(document.getElementById('february').value.toLowerCase()=='february'.toLowerCase()){
		document.getElementById('february').style.color='#AC5C90';
		document.getElementById('february').value='february'.toUpperCase();
	}else{
		document.getElementById('february').style.color='black';
		document.getElementById('february').value='february'.toLowerCase();
	}
});
